// ---------- Story (guion) storage: demo + user-created, in localStorage ----------

const SCRIPTS_KEY = 'moyos-learning-scripts-v1';

function loadScripts() {
  const raw = localStorage.getItem(SCRIPTS_KEY);
  const custom = raw ? JSON.parse(raw) : [];
  return [DEMO_SCRIPT, ...custom];
}

function saveCustomScript(script) {
  const raw = localStorage.getItem(SCRIPTS_KEY);
  const custom = raw ? JSON.parse(raw) : [];
  const idx = custom.findIndex(s => s.id === script.id);
  if (idx >= 0) custom[idx] = script;
  else custom.push(script);
  localStorage.setItem(SCRIPTS_KEY, JSON.stringify(custom));
}

function deleteCustomScript(id) {
  const raw = localStorage.getItem(SCRIPTS_KEY);
  const custom = raw ? JSON.parse(raw) : [];
  localStorage.setItem(SCRIPTS_KEY, JSON.stringify(custom.filter(s => s.id !== id)));
}

// A "card" (one sentence shown on screen) reads best around 4-6 lines.
// Very long run-on sentences get split at clause boundaries so no single
// card is a giant wall of text; very short ones get merged so a part
// never ends up with just "Bye. Bye." on its own.
const WORDS_MAX_CARD = 24;
const WORDS_MIN_CARD = 6;
const WORDS_PER_PART = 45;

function splitLongSentence(sentence) {
  const wordCount = sentence.split(/\s+/).filter(Boolean).length;
  if (wordCount <= WORDS_MAX_CARD) return [sentence];

  const clauses = sentence.split(/(?<=[,;:])\s+/);
  const chunks = [];
  let buf = '';
  for (const clause of clauses) {
    const candidate = buf ? buf + ' ' + clause : clause;
    if (buf && candidate.split(/\s+/).length > WORDS_MAX_CARD) {
      chunks.push(buf);
      buf = clause;
    } else {
      buf = candidate;
    }
  }
  if (buf) chunks.push(buf);

  // fold a too-small trailing fragment back into the previous one
  if (chunks.length > 1 && chunks[chunks.length - 1].split(/\s+/).length < WORDS_MIN_CARD) {
    const last = chunks.pop();
    chunks[chunks.length - 1] += ' ' + last;
  }
  return chunks;
}

// A line starting with "›" (or ">") is the client/customer speaking, not
// the narrator — the marker itself is never shown or read aloud.
const CLIENT_LINE_RE = /^[›>]\s*/;

// Split raw pasted text into sentences, then group them into reading
// "parts" sized by word count (not sentence count) so parts come out
// evenly sized and learnable instead of wildly different lengths.
// Speaker (seller/client) is tracked per line and carried through.
function buildScriptFromText(title, text) {
  const lines = text.split(/\r?\n/).map(l => l.trim()).filter(Boolean);

  const rawUnits = []; // { en, speaker }
  for (const line of lines) {
    const isClient = CLIENT_LINE_RE.test(line);
    const content = line.replace(CLIENT_LINE_RE, '').trim();
    if (!content) continue;
    const speaker = isClient ? 'client' : 'seller';
    content
      .split(/(?<=[.!?])\s+/)
      .map(s => s.trim())
      .filter(Boolean)
      .forEach(en => rawUnits.push({ en, speaker }));
  }

  const units = rawUnits.flatMap(u => splitLongSentence(u.en).map(en => ({ en, speaker: u.speaker })));

  const groups = [];
  let current = [];
  let currentWords = 0;
  for (const u of units) {
    const w = u.en.split(/\s+/).filter(Boolean).length;
    if (current.length && currentWords + w > WORDS_PER_PART) {
      groups.push(current);
      current = [];
      currentWords = 0;
    }
    current.push(u);
    currentWords += w;
  }
  if (current.length) groups.push(current);

  // fold a too-small trailing part into the previous one
  if (groups.length > 1) {
    const lastWords = groups[groups.length - 1].reduce((a, u) => a + u.en.split(/\s+/).length, 0);
    if (lastWords < WORDS_PER_PART * 0.35) {
      const last = groups.pop();
      groups[groups.length - 1].push(...last);
    }
  }

  const parts = groups.map((units, i) => ({
    label: String(i + 1),
    title: `Parte ${i + 1}`,
    sentences: units.map(u => ({ en: u.en, pron: null, es: null, speaker: u.speaker })),
  }));

  return { id: 'story-' + Date.now(), title, parts };
}

// ---------- Reading-test attempts (IndexedDB, keyed per script) ----------

const DB_NAME = 'karaoke-tests';
const STORE = 'attempts';

function _openDb() {
  return new Promise((resolve, reject) => {
    const rq = indexedDB.open(DB_NAME, 2);
    rq.onupgradeneeded = () => {
      const db = rq.result;
      if (!db.objectStoreNames.contains(STORE)) {
        db.createObjectStore(STORE, { keyPath: 'id', autoIncrement: true });
      }
    };
    rq.onsuccess = () => resolve(rq.result);
    rq.onerror = () => reject(rq.error);
  });
}

async function loadAttempts(scriptId) {
  const db = await _openDb();
  return new Promise((resolve, reject) => {
    const rq = db.transaction(STORE).objectStore(STORE).getAll();
    rq.onsuccess = () => {
      const rows = rq.result.filter(r => r.scriptId === scriptId);
      resolve(rows.map((r, i) => ({
        id: r.id, ms: r.ms, n: i + 1, date: r.date,
        url: r.blob ? URL.createObjectURL(r.blob) : null,
      })));
    };
    rq.onerror = () => reject(rq.error);
  });
}

async function saveAttempt(scriptId, ms, blob) {
  const db = await _openDb();
  return new Promise((resolve, reject) => {
    const rq = db.transaction(STORE, 'readwrite').objectStore(STORE).add({
      scriptId, ms, blob, date: new Date().toLocaleDateString('es'),
    });
    rq.onsuccess = () => resolve(rq.result);
    rq.onerror = () => reject(rq.error);
  });
}

async function deleteAttempt(id) {
  const db = await _openDb();
  return new Promise((resolve, reject) => {
    const tx = db.transaction(STORE, 'readwrite');
    tx.objectStore(STORE).delete(id);
    tx.oncomplete = () => resolve();
    tx.onerror = () => reject(tx.error);
  });
}
